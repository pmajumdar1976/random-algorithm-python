'''
You are given an m x n binary matrix grid. An island is a group of 1's (representing land) connected 4-directionally (horizontal or vertical.) You may assume all four edges of the grid are surrounded by water.

The area of an island is the number of cells with a value 1 in the island.

Return the maximum area of an island in grid. If there is no island, return 0.
'''

from typing import List, Set, Tuple, Optional

class Solution:
    def __init__(self):
        self.seen = {}
        
    def islandArea(self, coord: Tuple[int, int], indent='') -> int:
        x, y = coord
        if coord in self.seen:
            return self.seen[coord]

        #print('{}x={},y={}'.format(indent, x, y))
        if self.grid [y][x] == 0:
            #print('{}island={}'.format(indent, '{{}}'))
            self.seen[coord] = 0
            return 0

        area    = 0
        toVisit = [coord]
        while len(toVisit):
            c = toVisit.pop(0)
            x, y = c

            #print(' c = {}'.format(c))
            area += 1
            self.seen[c] = area # I agree that we are not storing the max area here
                                # but max area will be stored for at least one adjacent node

            if x > 0 and self.grid[y][x - 1]:
                newCoord = (x - 1, y)
                if newCoord not in self.seen:
                    #print('  1. append {}'.format(newCoord))
                    toVisit.append(newCoord)
                    self.seen[newCoord] = area # This will be updated later
                
            if x < (self.n - 1) and self.grid[y][x + 1]:
                newCoord = (x + 1, y)
                if newCoord not in self.seen:
                    #print('  2. append {}'.format(newCoord))
                    toVisit.append(newCoord)
                    self.seen[newCoord] = area # This will be updated later
                
            if y > 0 and self.grid[y - 1][x]:
                newCoord = (x, y - 1)
                if newCoord not in self.seen:
                    #print('  3. append {}'.format(newCoord))
                    toVisit.append(newCoord)
                    self.seen[newCoord] = area # This will be updated later

            if y < (self.m - 1) and self.grid[y + 1][x]:
                newCoord = (x, y + 1)
                if newCoord not in self.seen:
                    #print('  4. append {}'.format(newCoord))
                    toVisit.append(newCoord)
                    self.seen[newCoord] = area # This will be updated later
            
        return area
        
    def maxAreaOfIsland(self, grid: List[List[int]]) -> int:
        self.grid = grid
        self.m    = len(grid)
        self.n    = len(grid[0])
        
        maxArea = 0
        for y in range(self.m):
            for x in range(self.n):
                #self.visited= set({})
                coord = (x, y)
                area = self.islandArea(coord)
                #print('{} area = {}'.format(coord, area))
                if area > maxArea:
                    maxArea = area
        
        return maxArea

